'''
elif문은 if 밑에만 존재할 수 있으며,
여러 개를 넣을 수 있다.
'''

a = 100

if a < 10 :
  print("a is smaller than 10!")
  print("Yamu")

elif a < 50 :
  print("a is smaller than 50!")
  print("Yamu")

elif a < 300 :
  print("a is smaller than 300!")
  print("Yamu")

else :
  print("a is bigger or equal to 10")
  print("Jini")

print("wow!")
